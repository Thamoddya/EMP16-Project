        const image = document.querySelector('.image-container');
        image.addEventListener('mousemove', (e) => {
            const container = image.parentElement;
            const containerRect = container.getBoundingClientRect();

            const offsetX = (e.clientX - containerRect.left - containerRect.width / 2) / (containerRect.width / 2);
            const offsetY = (e.clientY - containerRect.top - containerRect.height / 2) / (containerRect.height / 2);

            image.style.transform = `rotateX(${offsetY * 20}deg) rotateY(${offsetX * 20}deg)`;
        });

        image.addEventListener('mouseleave', () => {
            image.style.transform = 'rotateX(0deg) rotateY(0deg)';
        });
